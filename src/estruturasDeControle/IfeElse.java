package estruturasDeControle;

public class IfeElse {

	public static void main(String[] args) {
		//desclarar uma variavel
		int x = 10;
		
		//implementado a estrutura de decisão
		if(x != 10) {
			System.out.println("x é igual ao valor de 10");
			
		}else {
			System.out.println("O valor de x é:" + x);
		}
	
	}
	
}
