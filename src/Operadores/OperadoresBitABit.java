package Operadores;

public class OperadoresBitABit {

	public static void main(String[] args) {
		// Operadores bit a bit
		int a=720, b=273;
		
		
		System.out.println("Operando a|b (quando o bit for 1 + 0 =1) = "+(a|b));
		System.out.println("Operando a^b (quando o bit for iguais fica 1 )= "+(a^b));
		int ano=1985;
		System.out.println("Operando a~b (quando o bit for Zero fica 1 )= "+(~ano));
		
		
		

	}

}
